                    if (_mainConnectionIsCreatedLocal)
                    {
                        // Open connection.
                        _mainConnection.Open();
                    }
                    else
                    {
                        if (_mainConnectionProvider.IsTransactionPending)
                        {
                            cmdToExecute.Transaction = _mainConnectionProvider.CurrentTransaction;
                        }
                    }
                    sqlDataReader = cmdToExecute.ExecuteReader();
                    baseEntityCollection.CollectionResponse = new List<GeneralPolicyRules>();
                    while (sqlDataReader.Read())
                    {
                        GeneralPolicyRules item = new GeneralPolicyRules();
                        //item.ID = Convert.ToInt32(sqlDataReader["GeneralPolicyRulesID"]);
                        if (DBNull.Value.Equals(sqlDataReader["GeneralPolicyRulesID"]) == false)
                        {

                            item.ID = Convert.ToInt32(sqlDataReader["GeneralPolicyRulesID"]);
                        }
                      
                        item.PolicyName = sqlDataReader["PolicyName"].ToString();
                        item.PolicyCode = sqlDataReader["PolicyCode"].ToString();
                        item.PolicyQuestionDescription = sqlDataReader["PolicyQuestionDescription"].ToString();
                        item.PolicyRange = sqlDataReader["PolicyRange"].ToString();
                        item.PolicyDefaultAnswer = sqlDataReader["PolicyDefaultAnswer"].ToString();
                        item.PolicyAnsType = sqlDataReader["PolicyAnsType"].ToString();
