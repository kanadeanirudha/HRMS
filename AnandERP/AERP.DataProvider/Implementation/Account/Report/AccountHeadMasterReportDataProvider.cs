                        AccountHeadMasterReport item = new AccountHeadMasterReport();
                        item.ID = Convert.ToByte(sqlDataReader["ID"]);
                        item.HeadCode = sqlDataReader["HeadCode"].ToString();
                        item.HeadName = sqlDataReader["HeadName"].ToString();
                        item.PrintingSequence = Convert.ToInt32(sqlDataReader["PrintingSequence"]);
                       

